public class InchesMm {

    private Integer in;
    private Integer mm;

    public Integer getIn() {
        return in;
    }

    public void setIn(Integer in) {
        this.in = in;
    }

    public Integer getMm() {
        return mm;
    }

    public void setMm(Integer mm) {
        this.mm = mm;
    }

    @Override
    public String toString() {
        return "InchesMm{" +
                "in=" + in +
                ", mm=" + mm +
                '}';
    }
}
