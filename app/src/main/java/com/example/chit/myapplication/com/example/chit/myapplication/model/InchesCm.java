public class InchesCm {

    private Integer in;
    private Integer cm;

    public Integer getIn() {
        return in;
    }

    public void setIn(Integer in) {
        this.in = in;
    }

    public Integer getCm() {
        return cm;
    }

    public void setCm(Integer cm) {
        this.cm = cm;
    }

    @Override
    public String toString() {
        return "InchesCm{" +
                "in=" + in +
                ", cm=" + cm +
                '}';
    }
}
